public class FirstClass {
    public static void main(String[] args) {
        System.out.print("*\n**\n***\n****\n*****\n******\n");
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
        int a = 10;
        int b = 5;
        double pi = 3.14159;
        double sum = a + b + pi;
        float ans = (a * b) / (a - b);
        System.out.println(sum);
        System.out.println(ans);
    }
}